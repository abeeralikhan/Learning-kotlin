package day_one

fun main() {
    // Type System
    // Kotlin can identify what type of value we are assigning to a variable
    // Therefore, we can eliminate the type while declaring a variable

    val name = "Abeer Khan"
    val age = 21
    val weight = 60.5f

    println("My name is $name, and I'm $age years old. I currently weigh around $weight kgs")

    // Note: We can not assign a null value without explicitly declare a type with "?" character.
}